import java.util.Properties

plugins {
    id("com.android.application")
    id("org.jetbrains.kotlin.android")
    id("rust")
}

val tauriProperties = Properties().apply {
    val propFile = file("tauri.properties")
    if (propFile.exists()) {
        propFile.inputStream().use { load(it) }
    }
}

val keyProperties = Properties().apply {
    val propFile = file("../key.properties")
    if (propFile.exists()) {
        propFile.inputStream().use { load(it) }
    }
}

// Variant selection: prefers `FRAK_VARIANT` env var (shared with iOS / build.rs),
// falls back to the legacy `-PappVariant=dev` (or `ORG_GRADLE_PROJECT_appVariant=dev`)
// Gradle property. Defaults to prod.
val appVariant = (System.getenv("FRAK_VARIANT")
    ?: project.findProperty("appVariant") as? String
    ?: "prod")
val isDevVariant = appVariant == "dev"
val appName = if (isDevVariant) "Frak Wallet Dev" else "Frak Wallet"
// `appLinkHost` still drives the per-variant Digital Asset Links URL (asset_statements).
// The custom-scheme + App Link intent-filters themselves are auto-generated by
// tauri-plugin-deep-link from `tauri.conf.json > plugins.deep-link.mobile`, so the
// `deepLinkScheme` placeholder is no longer needed.
val appLinkHost = if (isDevVariant) "wallet-dev.frak.id" else "wallet.frak.id"

android {
    compileSdk = 36
    ndkVersion = "29.0.14206865"
    namespace = "id.frak.wallet"
    defaultConfig {
        manifestPlaceholders["usesCleartextTraffic"] = "false"
        applicationId = "id.frak.wallet"
        minSdk = 28
        targetSdk = 36
        versionCode = tauriProperties.getProperty("tauri.android.versionCode", "1").toInt()
        versionName = tauriProperties.getProperty("tauri.android.versionName", "1.0")

        // Variant-aware resources — these replace the corresponding strings.xml entries.
        resValue("string", "app_name", appName)
        resValue("string", "main_activity_title", appName)
        resValue(
            "string",
            "asset_statements",
            """[{\"include\": \"https://$appLinkHost/.well-known/assetlinks.json\"}]"""
        )
    }
    signingConfigs {
        create("release") {
            storeFile = file(keyProperties.getProperty("storeFile", "../upload-keystore.jks"))
            storePassword = keyProperties.getProperty("storePassword", "")
            keyAlias = keyProperties.getProperty("keyAlias", "upload")
            keyPassword = keyProperties.getProperty("keyPassword", "")
        }
    }
    buildTypes {
        getByName("debug") {
            // `applicationIdSuffix` is also declared in tauri.conf.json
            // (`bundle.android.debugApplicationIdSuffix`); the Tauri CLI re-patches
            // this exact line on every `tauri android dev`/`build`. Keeping the
            // literal in sync here means the patch is a no-op (clean git tree).
            // Debug builds always install as `id.frak.wallet.dev` so they never
            // collide with a side-loaded prod build on the same device.
            applicationIdSuffix = ".dev"
            manifestPlaceholders["usesCleartextTraffic"] = "true"
            signingConfig = signingConfigs.getByName("release")
            isDebuggable = true
            isJniDebuggable = true
            isMinifyEnabled = false
            packaging {                jniLibs.keepDebugSymbols.add("*/arm64-v8a/*.so")
                jniLibs.keepDebugSymbols.add("*/armeabi-v7a/*.so")
                jniLibs.keepDebugSymbols.add("*/x86/*.so")
                jniLibs.keepDebugSymbols.add("*/x86_64/*.so")
            }
        }
        getByName("release") {
            // Dev release builds (TestFlight/Play internal track) carry the
            // `.dev` suffix so they install side-by-side with prod on the same device.
            if (isDevVariant) {
                applicationIdSuffix = ".dev"
            }
            signingConfig = signingConfigs.getByName("release")
            isMinifyEnabled = true
            proguardFiles(
                *fileTree(".") { include("**/*.pro") }
                    .plus(getDefaultProguardFile("proguard-android-optimize.txt"))
                    .toList().toTypedArray()
            )

            // Tell the firebase-crashlytics-gradle plugin to extract Rust debug symbols from the
            // unstripped `.so` (Cargo emits them with `strip = false` in [profile.release]) and
            // upload them via `uploadCrashlyticsSymbolFileRelease`. Without this, panic stacks in
            // Crashlytics are unsymbolicated (`art_quick_generic_jni_trampoline / <unknown>`).
            // AGP still strips the `.so` before packaging, so the shipped AAB stays small.
            configure<com.google.firebase.crashlytics.buildtools.gradle.CrashlyticsExtension> {
                nativeSymbolUploadEnabled = true
            }
        }
    }
    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }
    kotlinOptions {
        jvmTarget = "17"
    }
    buildFeatures {
        buildConfig = true
    }
}

rust {
    rootDirRel = "../../../"
}

